package com.kypnicholas.todolist;

import com.kypnicholas.todolist.datamodel.TodoData;
import com.kypnicholas.todolist.datamodel.TodoItem;
import javafx.fxml.FXML;
import javafx.scene.control.DatePicker;
import javafx.scene.control.TextArea;
import javafx.scene.control.TextField;

import java.time.LocalDate;

public class DialogController {

    @FXML
    private TextField shortDescriptionField;

    @FXML
    private TextArea detailsArea;

    @FXML
    private DatePicker deadlinePicker;

    //AFTER OK ON THE MODAL DIALOG IS PRESSED, WE WANT TO GATHER THE USER'S INPUT CREATE A NEW TO-DO ITEM AND ADD IT TO OUR LIST.
    public TodoItem processResults() {
        String shortDescription = shortDescriptionField.getText().trim();
        String details = detailsArea.getText().trim();
        LocalDate deadlineValue = deadlinePicker.getValue();

        TodoItem newItem = new TodoItem(shortDescription, details, deadlineValue);
        TodoData.getInstance().addTodoItem(newItem);
        return newItem;
    }

}
